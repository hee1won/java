package chap04.lecture.forloop;

public class C02For {
	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			System.out.println("hello");
		}

		int x = 6;
		for (int i = 0; i <= x; i++) {
			System.out.println("java");
		}

		for (int i = 5; i >= 0; i--) {
			System.out.println("spring");
		}

		int y = 14;
		for (int i = y; i >= 10; i--) {
			System.out.println("jsp");
		}
	}

}
